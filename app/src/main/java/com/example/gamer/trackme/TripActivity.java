package com.example.gamer.trackme;

public class TripActivity {
    private String meters;
    private String rate;

    public TripActivity() {
        //
    }

    public String getMeters() {
        return meters;
    }

    public void setMeters(String meters) {
        this.meters = meters;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }
}
